public class IntegerTypeVariables {
	
	public static void main(String[] args) {		
		int userAge = 50;
		System.out.println("A-) A idade do usuário Donatello é " + (userAge - 25) + " anos;");		
		System.out.println("B-) A idade do usuário Leonardo é " + (userAge / 2) + " anos;");		
		System.out.println("C-) A idade do usuário Michelangelo é " + ((userAge * 2) / 4) + " anos;");		
		System.out.print("D-) A idade do usuário Raphael é " + ((userAge + 50) - 75) + " anos.");
	}
}
